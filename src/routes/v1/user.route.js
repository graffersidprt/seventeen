const express = require('express');
const auth = require('../../middlewares/auth');
const upload = require('../../middlewares/multerUpload');
const validate = require('../../middlewares/validate');
const userValidation = require('../../validations/user.validation');
const userController = require('../../controllers/user.controller');

const router = express.Router();


router.route('/getUserName/:userId').post(auth(),validate(userValidation.geUserName),userController.getUserName);
router.route('/getChilds/:userId').get(auth('getChilds'), userController.getChildsNew);
router.route('/parent/childFriendlist/:childId').get(auth('getChilds'), userController.getChildFriendList);
router.route('/parent/details').get(auth(), userController.getParentDetails);
router.route('/approveChild').post(auth('approveChild'),userController.approveChild);
router
  .route('/')
  .post(validate(userValidation.createUser), userController.createUser)
  .get(auth(), userController.getUsers);

/*router
  .route('/getUserTest/:userId')
  .get( userController.getUserTest)*/
router.route('/upload-pic/:userId').patch( upload.single('avatar'), userController.uploadPicNew);

router
  .route('/:userId')
  .get(auth(), validate(userValidation.getUser), userController.getUser)
  .get(auth(), userController.updateUserInSetting)
  /*.get( userController.getUserTest)*/
 // .patch(auth(), upload.single('avatar'), userController.uploadPicNew)
  .put(auth(), validate(userValidation.updateUser), userController.updateUserNew)
  .delete(auth('manageUsers'), validate(userValidation.deleteUser), userController.deleteUser)
  .post(auth('manageUsers'), validate(userValidation.uploadPic), userController.uploadPic);

router.route('/blockuser/:userId').patch(auth(), userController.blockUser);
router
  .route('/update-setting/:userId')
  .post(auth(), userController.updateUserInSetting)
router.post('/get-profile', userController.getUser);

module.exports = router;

